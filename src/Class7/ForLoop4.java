package Class7;

public class ForLoop4 {
    public static void main(String[] args) {
        for (int i=1;i<10;i++){
            System.out.println("5 * "+i+" = "+(5*i));
        }
        System.out.println("**********************");
        for (int a=1;a<10;a++){
            System.out.println("3 * "+a+" = "+(3*a));
        }
    }
}
