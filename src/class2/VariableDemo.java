package class2;

public class VariableDemo {
    public static void main(String[] args) {
        String name="Sam";
        System.out.println(name);
        int age=22;
        // we can also use byte and short instead of int but int is just a common data type
        // but if we want to work according to rules we can use byte,short better to use int
        System.out.println(age);
        String city="Houstan Tx";
        System.out.println(city);
        int salary=120000;
        System.out.println(salary);
        char gender='m';
        System.out.println(gender);
        String number="+157-765-8754";
        System.out.println(number);
        boolean smart=true;
        System.out.println(smart);
    }
}
