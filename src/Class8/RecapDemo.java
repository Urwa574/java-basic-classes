package Class8;

public class RecapDemo {
    public static void main(String[] args) {
        int num=10;
        while (num>=2){
            System.out.println(num);
            num-=2;
        }
        System.out.println("________________________");
        int a=10;
        do {
            System.out.println(a);
            a -= 2; //a 2=a-2
        }while (a>=2);
        }
    }

